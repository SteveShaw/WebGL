function getBoundingBox(faceArray, vertexArray)
{
	var idx = faceArray[0];
	var minX = vertexArray[idx*3];
	var maxX = minX;
	var minY = vertexArray[idx*3+1];
	var maxY = minY;
	var minZ = vertexArray[idx*3+2];
	var maxZ = minZ;
	
	var curX = 0, curY = 0,curZ = 0;
	for(var fi = 1;fi<faceArray.length;++fi)
	{
		idx = faceArray[fi];
		curX = vertexArray[3*idx];
		curY = vertexArray[3*idx+1];
		curZ = vertexArray[3*idx+2];
		
		if(curX < minX) minX = curX;
		if(curX > maxX) maxX = curX;
		if(curY < minY) minY = curY;
		if(curY > maxY) maxY = curY;
		if(curZ < minZ) minZ = curZ;
		if(curZ > maxZ) maxZ = curZ;
	}
	
	return [minX,maxX,minY,maxY,minZ,maxZ];
}
	
